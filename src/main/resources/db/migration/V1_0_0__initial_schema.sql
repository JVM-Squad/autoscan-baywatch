CREATE TABLE FEEDS
(
    FEED_ID         INTEGER NOT NULL PRIMARY KEY,
    FEED_NAME       TEXT,
    FEED_URL        TEXT    NOT NULL,
    FEED_TAGS       TEXT,
    FEED_LAST_WATCH TEXT
);

CREATE TABLE NEWS
(
    NEWS_TITLE       TEXT    NOT NULL,
    NEWS_DESCRIPTION TEXT,
    NEWS_PUBLICATION TEXT    NOT NULL,
    NEWS_LINK        TEXT    NOT NULL UNIQUE,
    NEWS_STARED      INTEGER NOT NULL DEFAULT 0
);
