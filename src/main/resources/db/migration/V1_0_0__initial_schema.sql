CREATE TABLE FEEDS
(
    FEED_ID         INTEGER NOT NULL PRIMARY KEY,
    FEED_NAME       TEXT,
    FEED_URL        TEXT    NOT NULL,
    FEED_TAGS       TEXT,
    FEED_LAST_WATCH TEXT
);

CREATE TABLE FOLDER
(
    FOLD_ID   INTEGER NOT NULL PRIMARY KEY,
    FOLD_NAME TEXT
);

CREATE TABLE FEEDS_FOLDER
(
    FEFO_FOLD_ID INTEGER NOT NULL,
    FEFO_FEED_ID INTEGER NOT NULL,

    PRIMARY KEY (FEFO_FOLD_ID, FEFO_FEED_ID),
    CONSTRAINT FK_FEFO_FEED FOREIGN KEY (FEFO_FEED_ID) REFERENCES FEEDS (FEED_ID),
    CONSTRAINT FK_FEFO_FOLD FOREIGN KEY (FEFO_FOLD_ID) REFERENCES FOLDER (FOLD_ID)
);

CREATE TABLE NEWS
(
    NEWS_FEED_ID     INTEGER NOT NULL PRIMARY KEY,
    NEWS_TITLE       TEXT    NOT NULL,
    NEWS_DESCRIPTION TEXT,
    NEWS_PUBLICATION TEXT    NOT NULL,
    NEWS_LINK        TEXT    NOT NULL UNIQUE,
    NEWS_STARED      INTEGER NOT NULL DEFAULT 0
);

